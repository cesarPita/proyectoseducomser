    private void cargarTabla() {
        String head[] = {"ID", "Categoria", "Descripcion"};
        DefaultTableModel model = new DefaultTableModel(null, head);
        
        for (Categoria c : cdao.findAll()) {
            model.addRow(new Object[]{
                c.getId(),
                c.getCategoria(),
                c.getDescripcion()
            });
        }
        
        tblCategorias.setModel(model);
        tblCategorias.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        TableColumnModel columna= tblCategorias.getColumnModel();
        columna.getColumn(0).setPreferredWidth(50);     
        columna.getColumn(1).setPreferredWidth(150);     
        columna.getColumn(2).setPreferredWidth(350);     
    }

