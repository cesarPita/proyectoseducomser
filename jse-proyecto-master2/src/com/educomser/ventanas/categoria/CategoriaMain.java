    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int index = tblCategorias.getSelectedRow();
        if (index != -1) {
            int input = JOptionPane.showConfirmDialog(rootPane, "Esta seguro de eliminar el registro seleccionado?", null, JOptionPane.YES_NO_OPTION);
            if (input == 0) {
                int id = (int) tblCategorias.getValueAt(index, 0);
                cdao.delete(id);
                cargarTabla();
                JOptionPane.showMessageDialog(null, "El registro fue eliminado.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro de la tabla.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        CategoriaModal modal = new CategoriaModal(this, rootPaneCheckingEnabled);
        modal.setTitulo("Nueva Categoria");
        modal.setVisible(true);
        Categoria categoria = modal.getCategoria();
        if (categoria != null) {
            cdao.save(categoria);
            cargarTabla();
            JOptionPane.showMessageDialog(null, "La categoria fue adicionado.");
        }

    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
           int index = tblCategorias.getSelectedRow();
        if (index != -1) {
            int id = (int) tblCategorias.getValueAt(index, 0);
            Categoria categoria = cdao.findById(id);
            CategoriaModal modal = new CategoriaModal(this, rootPaneCheckingEnabled);
            modal.setTitulo("Editar Categoria");
            modal.editarCategoria(categoria);
            modal.setVisible(true);
            categoria = modal.getCategoria();
            if (categoria != null) {
                cdao.update(categoria);
                cargarTabla();
                JOptionPane.showMessageDialog(null, "La categoria fue editada exitosamente.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro de la tabla.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

